//'typeof' is used to check the data structure, or type, of a variable
//  useful in debugging code with multiple data types
//Extra care should be used when working iwth external data in a JSON (JS Object Notation) object
/**
 * JS recognizes 6 primitive data types:
 *  Boolean
 *  Null
 *  Undefined
 *  Number
 *  String
 *  Symbol (new with ES6)
 * 
 * One type for mutable items:
 *  Object
 */

let seven = 7;
let three = "3";
console.log(seven + three);
// Only change code below this line

console.log(typeof seven);
console.log(typeof three);