//ES6 adds the spread operator, which allows copying all elements of an array into a new array
//Syntax: arr2 = [...arr1];

function copyMachine(arr, num) {
  let newArr = [];
  while (num >= 1) {
    // Only change code below this line
    newArr.push([...arr]);
    // Only change code above this line
    num--;
  }
  return newArr;
}

console.log(copyMachine([true, false, true], 2));