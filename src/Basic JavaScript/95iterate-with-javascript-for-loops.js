//For loops are most common
//Syntax: for (a; b; c)
//  a - initialization statement; executed once
//  b - condition statement; evaluated at every iteration
//    loop will break if false
//  c - final expression; executed at the end of each iteration

// Setup
var myArray = [];

// Only change code below this line
for (let i = 1; i <= 5; i++) {
  myArray.push(i);
}