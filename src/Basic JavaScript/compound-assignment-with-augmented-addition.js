//Common in programming to use assignments to modify variable contents
//Everything to right of equals is evaluated first
//'a += 3' the same as 'a = a + 3'

var a = 3;
var b = 17;
var c = 12;

// Only change code below this line
/* Original code: 
a = a + 12;
b = 9 + b;
c = c + 7;
*/

//Solution
a += 12;
b += 9;
c += 7;